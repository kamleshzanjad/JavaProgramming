package session3;

public class _2_EnhanceForLoop {

    public static void main(String args[]) {

        // declaring an array and assigning values
        int arr[] = {12, 13, 14, 44};


        // traversing the array with for-each loop
        for (int i : arr) {
            System.out.println(i);
        }

        // traversing the array with conventional for loop
        System.out.println("\n \n Using ocnvention For loop");
        int len = arr.length;
        int counter;
        for (counter = 0; counter < len; counter++) {
            System.out.println(counter);
        }
    }

}


